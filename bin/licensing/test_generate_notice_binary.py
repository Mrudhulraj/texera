"""Unit tests for generate_notice_binary.

Run via:
    python3 -m unittest bin.licensing.test_generate_notice_binary
or (as CI does):
    python3 -m unittest discover -s bin/licensing -p "test_*.py"

Stdlib only — no pytest, no project deps — so it runs in any CI job that has
Python set up. Synthetic jars are built in temp dirs with zipfile.
"""
from __future__ import annotations

import subprocess
import sys
import tempfile
import unittest
import zipfile
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent))
import generate_notice_binary as gnb  # noqa: E402


def make_jar(
    path: Path,
    *,
    notice: str | None = None,
    group: str | None = None,
    artifact: str | None = None,
    version: str | None = None,
    extra: dict[str, str] | None = None,
) -> Path:
    """Write a minimal jar (zip) with an optional META-INF/NOTICE and an
    optional Maven pom.properties."""
    with zipfile.ZipFile(path, "w") as zf:
        zf.writestr("a/b/C.class", "")  # some payload
        if notice is not None:
            zf.writestr("META-INF/NOTICE", notice)
        if group and artifact and version:
            zf.writestr(
                f"META-INF/maven/{group}/{artifact}/pom.properties",
                f"#Generated by Maven\ngroupId={group}\nartifactId={artifact}\nversion={version}\n",
            )
        for name, content in (extra or {}).items():
            zf.writestr(name, content)
    return path


# --- pure-function tests ---------------------------------------------------


class StripVersionFromFilename(unittest.TestCase):
    def test_plain_dotted_version(self):
        self.assertEqual(
            gnb._strip_version_from_filename("org.apache.lucene.lucene-memory-8.7.0.jar"),
            "org.apache.lucene.lucene-memory",
        )

    def test_hyphenated_qualifier(self):
        # guava ships as <coords>-33.0.0-jre.jar — the -jre qualifier is part
        # of the version and must be stripped too.
        self.assertEqual(
            gnb._strip_version_from_filename("com.google.guava.guava-33.0.0-jre.jar"),
            "com.google.guava.guava",
        )

    def test_dotted_qualifier(self):
        self.assertEqual(
            gnb._strip_version_from_filename(
                "org.eclipse.jetty.websocket.websocket-api-9.4.40.v20210413.jar"
            ),
            "org.eclipse.jetty.websocket.websocket-api",
        )

    def test_digit_in_artifact_name(self):
        # 'jdk8' must not be mistaken for a version start.
        self.assertEqual(
            gnb._strip_version_from_filename(
                "com.fasterxml.jackson.datatype.jackson-datatype-jdk8-2.16.1.jar"
            ),
            "com.fasterxml.jackson.datatype.jackson-datatype-jdk8",
        )

    def test_no_version_just_drops_jar(self):
        self.assertEqual(gnb._strip_version_from_filename("foo.jar"), "foo")


class ArtifactLabel(unittest.TestCase):
    def test_prefers_pom_properties(self):
        with tempfile.TemporaryDirectory() as d:
            jar = make_jar(
                Path(d) / "org.example.widget-9.9.9.jar",
                notice="x",
                group="org.example",
                artifact="widget",
                version="9.9.9",
            )
            self.assertEqual(gnb.artifact_label(jar), "org.example.widget")

    def test_cross_versioned_scala_artifact(self):
        with tempfile.TemporaryDirectory() as d:
            jar = make_jar(
                Path(d) / "org.scalatest.scalatest_2.13-3.2.17.jar",
                notice="x",
                group="org.scalatest",
                artifact="scalatest_2.13",
                version="3.2.17",
            )
            self.assertEqual(gnb.artifact_label(jar), "org.scalatest.scalatest_2.13")

    def test_falls_back_to_filename_without_pom(self):
        with tempfile.TemporaryDirectory() as d:
            jar = make_jar(Path(d) / "com.google.guava.guava-33.0.0-jre.jar", notice="x")
            self.assertEqual(gnb.artifact_label(jar), "com.google.guava.guava")

    def test_label_never_contains_version(self):
        with tempfile.TemporaryDirectory() as d:
            jar = make_jar(
                Path(d) / "org.example.thing-1.2.3.jar",
                notice="x",
                group="org.example",
                artifact="thing",
                version="1.2.3",
            )
            self.assertNotIn("1.2.3", gnb.artifact_label(jar))


class IsNoticeEntry(unittest.TestCase):
    def test_classification(self):
        self.assertTrue(gnb.is_notice_entry("NOTICE"))
        self.assertTrue(gnb.is_notice_entry("notice.txt"))
        self.assertTrue(gnb.is_notice_entry("META-INF/NOTICE"))
        self.assertTrue(gnb.is_notice_entry("META-INF/NOTICE.md"))
        self.assertFalse(gnb.is_notice_entry("LICENSE"))
        self.assertFalse(gnb.is_notice_entry("META-INF/DEPENDENCIES"))
        self.assertFalse(gnb.is_notice_entry("META-INF/maven/g/a/pom.properties"))
        self.assertFalse(gnb.is_notice_entry("com/foo/Notice.class"))


class ExtractNoticeBlob(unittest.TestCase):
    def test_extracts_and_normalizes_crlf(self):
        with tempfile.TemporaryDirectory() as d:
            jar = make_jar(Path(d) / "x.jar", notice="hello\r\nworld\r\n")
            self.assertEqual(gnb.extract_notice_blob(jar), "hello\nworld")

    def test_no_notice_returns_none(self):
        with tempfile.TemporaryDirectory() as d:
            jar = make_jar(Path(d) / "x.jar")  # no notice
            self.assertIsNone(gnb.extract_notice_blob(jar))

    def test_empty_notice_returns_none(self):
        with tempfile.TemporaryDirectory() as d:
            jar = make_jar(Path(d) / "x.jar", notice="   \n  ")
            self.assertIsNone(gnb.extract_notice_blob(jar))


class ProjectNameForCluster(unittest.TestCase):
    def test_single(self):
        self.assertEqual(gnb.project_name_for_cluster(["org.example.widget"]), "org.example.widget")

    def test_common_prefix(self):
        self.assertEqual(
            gnb.project_name_for_cluster(
                ["org.apache.lucene.lucene-core", "org.apache.lucene.lucene-memory"]
            ),
            "org.apache.lucene",
        )

    def test_dedups_repeated_labels(self):
        self.assertEqual(gnb.project_name_for_cluster(["a.b", "a.b"]), "a.b")


class EmitBlock(unittest.TestCase):
    def test_no_bundled_jars_line_and_has_heading(self):
        block = gnb.emit_block("org.example.widget", "the notice text")
        self.assertIn("org.example.widget", block)
        self.assertIn("the notice text", block)
        self.assertNotIn("Bundled jars", block)
        # Version-less heading is sandwiched between two separators.
        self.assertIn(gnb.SEP + "\norg.example.widget\n" + gnb.SEP, block)


# --- end-to-end CLI test ---------------------------------------------------


class EndToEndCli(unittest.TestCase):
    def _run(self, out: Path, libdir: Path, notice: Path, extras: Path | None = None):
        cmd = [sys.executable, str(Path(gnb.__file__)), str(out), str(libdir),
               "--project-notice", str(notice)]
        if extras:
            cmd += ["--extras", str(extras)]
        subprocess.run(cmd, check=True, capture_output=True, text=True)
        return out.read_text()

    def test_deterministic_versionless_output(self):
        with tempfile.TemporaryDirectory() as d:
            d = Path(d)
            lib = d / "lib"
            lib.mkdir()
            # two jars sharing nothing; one with a pom, one without.
            make_jar(lib / "org.example.alpha-1.0.0.jar", notice="Alpha NOTICE",
                     group="org.example", artifact="alpha", version="1.0.0")
            make_jar(lib / "com.example.beta-2.5.0.jar", notice="Beta NOTICE")
            # a texera-own jar must be skipped entirely.
            make_jar(lib / "org.apache.texera.dao-9.9.9.jar", notice="should be skipped")
            notice = d / "NOTICE"
            notice.write_text("Apache Texera (Incubating)\nCopyright 2025\n")

            out = d / "out.txt"
            text1 = self._run(out, lib, notice)
            text2 = self._run(out, lib, notice)

            self.assertEqual(text1, text2)  # deterministic
            self.assertTrue(text1.startswith("Apache Texera (Incubating)"))
            self.assertIn("org.example.alpha", text1)   # version-less heading
            self.assertIn("com.example.beta", text1)
            self.assertIn("Alpha NOTICE", text1)
            self.assertNotIn("Bundled jars", text1)
            self.assertNotIn("1.0.0", text1)            # no versions leak in
            self.assertNotIn("should be skipped", text1)  # texera jar skipped

    def test_extras_are_appended(self):
        with tempfile.TemporaryDirectory() as d:
            d = Path(d)
            lib = d / "lib"
            lib.mkdir()
            make_jar(lib / "org.example.alpha-1.0.0.jar", notice="Alpha NOTICE",
                     group="org.example", artifact="alpha", version="1.0.0")
            notice = d / "NOTICE"
            notice.write_text("Project NOTICE\n")
            extras = d / "extras.txt"
            extras.write_text("EXTRA PYTHON ATTRIBUTION BLOCK\n")

            text = self._run(d / "out.txt", lib, notice, extras=extras)
            self.assertIn("EXTRA PYTHON ATTRIBUTION BLOCK", text)


if __name__ == "__main__":
    unittest.main()
